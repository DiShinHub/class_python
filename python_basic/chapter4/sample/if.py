# if
a = "내일은 시험입니다."

if a == "내일은 시험입니다.":
    print("집에 가서 공부해야죠!")

# if elif else
a = "내일은 시험이 아닙니다."
if a == "내일은 시험입니다.":
    print("집에 가서 공부해야죠!")

elif a == "내일은 시험이 아닙니다.":
    print("그럼 놀아아죠!")

else:
    print("그럼 뭔데?")


